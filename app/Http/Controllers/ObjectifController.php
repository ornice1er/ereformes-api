<?php

namespace App\Http\Controllers;

use App\Http\Repositories\ObjectifRepository;
use App\Http\Requests\Objectif\StoreObjectifRequest;
use App\Http\Requests\Objectif\UpdateObjectifRequest;
use App\Services\LogService;
use App\Utilities\Common;
use Illuminate\Http\Request;
use OpenApi\Attributes as OA;

class ObjectifController extends Controller
{
    /**
     * The objectif repository being queried.
     *
     * @var ObjectifRepository
     */
    protected $objectifRepository;

    protected $ls;

    public function __construct(ObjectifRepository $objectifRepository, LogService $ls)
    {
        $this->objectifRepository = $objectifRepository;
        $this->ls = $ls;

        //$this->middleware('auth:api')->except(['getNotified', 'show']);

    }

    /** @OA\Get(
     *      path="/objectifs",
     *      operationId="Objectif list",
     *      tags={"Objectif"},
     *       security={{"JWT":{}}},
     *      summary="Return objectif data",
     *      description="Get all objectifs",
     *
     *      @OA\Parameter(
     *          name="name",
     *          in="query",
     *          description="Can be used for filtering data by name",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="project_id",
     *          in="query",
     *          description="Project ID",
     *
     *          @OA\Schema(
     *              type="integer"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="role",
     *          in="query",
     *          description="Objectif Role ID",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="categorie",
     *          in="query",
     *          description="Can be used for filtering data by categorie| ANIMATRICE,RESPONSABLE",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *          ),
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
