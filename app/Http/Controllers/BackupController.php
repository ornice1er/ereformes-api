<?php

namespace App\Http\Controllers;

use App\Http\Repositories\BackupRepository;
use App\Http\Requests\Backup\StoreBackupRequest;
use App\Http\Requests\Backup\UpdateBackupRequest;
use App\Services\LogService;
use App\Utilities\Common;
use Illuminate\Http\Request;
use OpenApi\Attributes as OA;

class BackupController extends Controller
{
    /**
     * The backup repository being queried.
     *
     * @var BackupRepository
     */
    protected $backupRepository;

    protected $ls;

    public function __construct(BackupRepository $backupRepository, LogService $ls)
    {
        $this->backupRepository = $backupRepository;
        $this->ls = $ls;

        //$this->middleware('auth:api')->except(['getNotified', 'show']);

    }

    /** @OA\Get(
     *      path="/backups",
     *      operationId="Backup list",
     *      tags={"Backup"},
     *       security={{"JWT":{}}},
     *      summary="Return backup data",
     *      description="Get all backups",
     *
     *      @OA\Parameter(
     *          name="name",
     *          in="query",
     *          description="Can be used for filtering data by name",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="project_id",
     *          in="query",
     *          description="Project ID",
     *
     *          @OA\Schema(
     *              type="integer"
     *          )
     *      ),
     *
     * @OA\Parameter(
     *          name="role",
     *          in="query",
     *          description="Backup Role ID",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="categorie",
     *          in="query",
     *          description="Can be used for filtering data by categorie| ANIMATRICE,RESPONSABLE",
     *          required=false,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *          ),
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
     *          @OA\JsonContent(ref="#/components/schemas/Backup"),
