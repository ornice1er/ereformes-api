     *
     *      @OA\Parameter(
     *          name="id",
     *          in="path",
     *          description="Couverture ID",
     *          required=true,
     *
     *          @OA\Schema(
     *              type="string"
     *          )
     *      ),
     *
     *      @OA\Parameter(
     *          name="state",
     *          in="path",
     *          description="Couverture state",
