     *              type="string"
     *          )
     *      ),
     *      summary="Change Couverture state",
     *      description="Change Couverture state by ID",
     *
     *      @OA\Response(
     *          response=200,
     *          description="Successful operation",
     *
     *          @OA\JsonContent(ref="#/components/schemas/Couverture"),
     *
     *          @OA\XmlContent(ref="#/components/schemas/Couverture")
