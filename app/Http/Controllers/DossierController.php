     *          response=404,
     *          description="Not found"
     *      ),
     *      @OA\Response(
     *          response=500,
     *          description="Server Error"
     *      )
     * )
     */
    public function changeState($id, $state)
    {
        $message = 'Changement de l\'état d\'un utilisateur';

        try {
            $result = $this->dossierRepository->setStatus($id, $state);
            $statusMessage = $state == 1 ? 'activé' : 'désactivé';
            $this->ls->trace(['action_name' => $message, 'description' => json_encode($result)]);

            return Common::success("Utilisateur $statusMessage avec succès", $result);
        } catch (\Throwable $th) {
            $this->ls->trace(['action_name' => $message, 'description' => $th->getMessage()]);

            return Common::error($th->getMessage(), []);
        }
    }

    /** @OA\Post(
     *      path="/dossiers-search",
     *      operationId="Dossier searching",
     *      tags={"Dossier"},
     *       security={{"JWT":{}}},
     *      summary="Return list of Dossier respecting term",
     *      description="Get all filtered dossiers using term",
     *
     *     @OA\Response(
     *         response=200,
     *         description="Successful operation",
     *
     *         @OA\JsonContent(ref="#/components/schemas/Dossier"),
     *
     *         @OA\XmlContent(ref="#/components/schemas/Dossier")
     *     ),
     *
     *     @OA\RequestBody(
     *         description="Body request",
     *         required=true,
